function KPIplots()

% TITLES FOR FIGURES
titles = { ...
    'I=0.0', ...              
    'I=0.5', ...                
    'I=1.0', ...              
    'I=2.0', ...                
    'I=3.0', ...          
    'I=4.0', ...              
    'I=5.0', ...                
    'I=6.0', ...
    'I=7.0', ...              
    'I=8.0', ...                
    'I=9.0', ...
    'I=10.0'
    };
% NAMES OF SCENARIOS
scenarios = { ...
       'scenBaseR_MVD'
    };
%% Compute TTS and num inc table

itau = 'tau=00';

for ri = 1: numel(titles)
        
        fname = sprintf('%s_%s_%s.mat',scenarios{1}, titles{ri}, itau);
        load(fname,'Sim');
        
          %save the data in fields
          tbl(ri,1) = Sim.Edie.TTS;
%         mineKPI = setfield(mineKPI, fname, 'speed', {iscenario}, Sim.Edie.u);
%         mineKPI = setfield(mineKPI, fname, 'TTS'  , {iscenario}, Sim.Edie.TTS);
%         mineKPI = setfield(mineKPI, fname, 'NoA'  , {iscenario}, Sim.numCollisions);
end


%% Create Figures
disp('TTS KPI ...');
figttskpi = figure; %figtraj.WindowStyle = 'docked'; 
figttskpi.Name = sprintf('TTS KPI');
figttskpi.Position = [50,50,1536,768];
cla; axSim = gca;
for i = 1:numel(titles)
    atitle = titles(i);
    %bTaustr = sprintf('tau%.2d',taus(i));
    subplot(3,4,i);
    spider_plot_R2019b(getfield(mineKPI,bTaustr,'TTS'),...
    'AxesLabels', titles,...
    'AxesInterval', 4,...
    'AxesDisplay', 'one',...
    'AxesFont', 'Times New Roman',...
    'LabelFont', 'Times New Roman',...
    'AxesFontSize', 12,...
    'LabelFontSize', 10,...
    'LineStyle', {'--'},...
    'LineWidth', [1],...
    'Marker', {'o'},...
    'MarkerSize', [8],...
    'FillOption', 'on',...
    'FillTransparency', 0.2,...
    'Color', [1, 0, 0]...
    );
    title(atitle);

end

saveas(figttskpi  ,'TTSKPI.png');

disp('FLOW KPI ...');
figflowkpi = figure; %figtraj.WindowStyle = 'docked'; 
figflowkpi.Name = sprintf('FLOW KPI');
figflowkpi.Position = [50,50,1536,768];
cla; axSim = gca;
for i = 1:numel(taus)
    aTaustr = sprintf('tau=%.2d',taus(i));
    bTaustr = sprintf('tau%.2d',taus(i));
    subplot(2,3,i);
    spider_plot_R2019b(getfield(mineKPI,bTaustr,'flow'),...
    'AxesLabels', titles,...
    'AxesInterval', 2,...
    'AxesDisplay', 'one',...
    'AxesFont', 'Times New Roman',...
    'LabelFont', 'Times New Roman',...
    'AxesFontSize', 12,...
    'LabelFontSize', 10,...
    'LineStyle', {'--'},...
    'LineWidth', [1],...
    'Marker', {'o'},...
    'MarkerSize', [8],...
    'FillOption', 'on',...
    'FillTransparency', 0.2,...
    'Color', [0, 1, 0]...
    );
    title(aTaustr);
end
saveas(figflowkpi  ,'FLOWKPI.png');

disp('SPEED KPI ...');
figspeedkpi = figure; %figtraj.WindowStyle = 'docked'; 
figspeedkpi.Name = sprintf('SPEED KPI');
figspeedkpi.Position = [50,50,1536,768];
cla; axSim = gca;
for i = 1:numel(taus)
    aTaustr = sprintf('tau=%.2d',taus(i));
    bTaustr = sprintf('tau%.2d',taus(i));
