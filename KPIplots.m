saveas(figspeedkpi  ,'SPEEDKPI.png');

disp('NUMBER OF ACCIDENTS KPI ...');
fignoakpi = figure; %figtraj.WindowStyle = 'docked'; 
fignoakpi.Name = sprintf('ACCIDENTS KPI');
fignoakpi.Position = [50,50,1536,768];
cla; axSim = gca;
for i = 1:numel(taus)
 aTaustr = sprintf('tau=%.2d',taus(i));
    bTaustr = sprintf('tau%.2d',taus(i));
    subplot(2,3,i);
    minaxes = min(getfield(mineKPI,bTaustr,'NoA'));
    maxaxes = max(getfield(mineKPI,bTaustr,'NoA'));
    if maxaxes == 0
        maxaxes = 1;
        matmax(1:length(getfield(mineKPI,bTaustr,'NoA'))) = maxaxes;
    else
        matmax(1:length(getfield(mineKPI,bTaustr,'NoA'))) = maxaxes;
    end
    
    matmin(1:length(getfield(mineKPI,bTaustr,'NoA'))) = minaxes;
    
    spider_plot_R2019b(getfield(mineKPI,bTaustr,'NoA'),...
    'AxesLimits',[matmin;matmax],...
    'AxesLabels', titles,...
    'AxesInterval', 2,...
    'AxesDisplay', 'one',...
    'AxesFont', 'Times New Roman',...
    'LabelFont', 'Times New Roman',...
    'AxesFontSize', 12,...
    'LabelFontSize', 10,...
    'LineStyle', {'--'},...
    'LineWidth', [1],...
    'Marker', {'o'},...
    'MarkerSize', [8],...
    'FillOption', 'on',...
    'FillTransparency', 0.2,...
    'Color', [1, 1, 0]...
    );
    title(aTaustr);
end
saveas(fignoakpi  ,'NoAKPI.png');
end