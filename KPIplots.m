    'AxesDisplay', 'one',...
    'AxesFont', 'Times New Roman',...
    'LabelFont', 'Times New Roman',...
    'AxesFontSize', 12,...
    'LabelFontSize', 10,...
    'LineStyle', {'--'},...
    'LineWidth', [1],...
    'Marker', {'o'},...
    'MarkerSize', [8],...
    'FillOption', 'on',...
    'FillTransparency', 0.2,...
    'Color', [0, 0, 1]...
    );
    title(aTaustr);
